const monthsReg = [
    /Январ([(ь|я|е|ю|ем|ём)]{0,2})/i,
    /Феврал([(ь|я|е|ю|ем|ём)]{0,2})/i,
    /Март([(а|е|у|ом)]{0,2})/i,
    /Апрел([(ь|я|е|ю|ем|е)]{0,2})/i,
    /Ма([(е|й|я|ю|ем)]{1,2})/i,
    /Июн([(ь|я|е|ю|ем|е)]{0,2})/i,
    /Июл([(ь|я|е|ю|ем|е)]{0,2})/i,
    /Август([(а|е|у|ом)]{0,2})/i,
    /Сентябр([(ь|я|е|ю|ем|ём)]{0,2})/i,
    /Октябр([(ь|я|е|ю|ем|ём)]{0,2})/i,
    /Ноябр([(ь|я|е|ю|ем|ём)]{0,2})/i,
    /Декабр([(ь|я|е|ю|ем|ём)]{0,2})/i,
];

/**
 * Get index of month name
 * @param {string} month
 * return {number}
 */
export function getMonthNumber(month) {
    for (const i in monthsReg) {
        const reg = monthsReg[i];
        if (month.match(reg)) {
            return Number(i);
        }
    }
}

